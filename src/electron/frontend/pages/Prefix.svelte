<script lang="ts">
	import { onMount } from "svelte";
	import {
		PickFolder,
		RunPrefixTool,
		ScanProtonVersions,
		ListPrefixes,
		GetPrefixBaseDir,
		CreatePrefix,
		SavePrefixConfig,
		LoadPrefixConfig,
		GetSystemToolsStatus,
	} from "../api";
	import Dropdown from "../components/Dropdown.svelte";
	import ConfigForm from "../components/ConfigForm.svelte";
	import ToolButton from "../components/ToolButton.svelte";
	import type { launcher } from "../models";
	import { notifications } from "../notificationStore";

	// Prefix Management
	let prefixPath = "";
	let baseDir = "";
	let availablePrefixes: string[] = [];
	let newPrefixName = "";

	// Proton
	let protonVersions: launcher.ProtonTool[] = [];
	let protonOptions: string[] = [];
	let selectedProton = "";

	// UI State
	let isLoading = false;
	let runningToolName = "";
	let systemStatus: launcher.SystemToolsStatus = { hasGamescope: false, hasMangoHud: false, hasGameMode: false };

	// Config
	let prefixOptions: launcher.LaunchOptions = {
		GamePath: "",
		LauncherPath: "",
		UseGameExe: false,
		PrefixPath: "",
		ProtonPattern: "",
		ProtonPath: "",
		CustomArgs: "",
		EnableGamescope: false,
		GamescopeW: "1920",
		GamescopeH: "1080",
		GamescopeR: "60",
		EnableMangoHud: false,
		EnableGamemode: false,
		EnableLsfgVk: false,
		LsfgMultiplier: "2",
		LsfgPerfMode: false,
		LsfgDllPath: "",
		LsfgGpu: "",
		LsfgFlowScale: "0.8",
		LsfgPacing: "none",
		LsfgAllowFp16: false,
		EnableMemoryMin: false,
		MemoryMinValue: "4G",
	};

	async function refreshPrefixes() {
		try {
			const list = await ListPrefixes();
			availablePrefixes = Array.isArray(list) ? list : [];
			baseDir = await GetPrefixBaseDir();
			if (!prefixPath && availablePrefixes.length > 0) {
				selectPrefix(availablePrefixes[0]);
			} else if (!prefixPath) {
				prefixPath = baseDir + "/Default";
			}
		} catch (err) {
			console.error(err);
			availablePrefixes = [];
		}
	}

	onMount(async () => {
		try {
			const [tools, status] = await Promise.all([ScanProtonVersions(), GetSystemToolsStatus()]);
			systemStatus = status;
			protonVersions = tools;
			protonOptions = protonVersions.map((t) => t.DisplayName);
			if (protonVersions.length > 0) {
				selectedProton = protonOptions[0];
			}
			await refreshPrefixes();
		} catch (err) {
			console.error(err);
		}
	});

	async function selectPrefix(name: string) {
		prefixPath = baseDir + "/" + name;
		try {
			const config = await LoadPrefixConfig(name);
			if (config) {
				prefixOptions = { ...prefixOptions, ...config };

				if (config.ProtonPattern) {
					const match = protonVersions.find(
						(p) => p.Name === config.ProtonPattern || p.DisplayName === config.ProtonPattern,
					);
					if (match) selectedProton = match.DisplayName;
					else if (config.ProtonPattern) selectedProton = config.ProtonPattern;
				}
			} else {
				resetOptions();
			}
		} catch (e) {
			resetOptions();
		}
	}

	function resetOptions() {
		prefixOptions.CustomArgs = "";
		prefixOptions.EnableGamescope = false;
		prefixOptions.EnableMangoHud = false;
		prefixOptions.EnableGamemode = false;
		prefixOptions.EnableLsfgVk = false;
	}

	async function handleSaveConfig() {
		if (!prefixPath) return;
		const name = prefixPath.split("/").pop() || "Default";

		// Update proton info in options
		const tool = protonVersions.find((p) => p.DisplayName === selectedProton);
		let cleanName = selectedProton;
		if (cleanName.startsWith("(Steam) ")) cleanName = cleanName.substring(8);

		prefixOptions.ProtonPattern = cleanName;
		prefixOptions.ProtonPath = tool ? tool.Path : "";

		try {
			await SavePrefixConfig(name, prefixOptions);
			notifications.success("Prefix defaults saved!");
		} catch (err) {
			notifications.error("Failed to save: " + err);
		}
	}

	async function handleBrowse() {
		try {
			const path = await PickFolder();
			if (path) prefixPath = path;
		} catch (err) {
			console.error(err);
		}
	}

	async function handleCreatePrefix() {
		if (!newPrefixName) return;
		const name = newPrefixName;
		try {
			await CreatePrefix(name);
			newPrefixName = "";
			await refreshPrefixes();
			selectPrefix(name);
			notifications.success(`Created prefix "${name}"`);
		} catch (err) {
			notifications.error(`Failed to create: ${err}`);
		}
	}

	function handleProtonChange(value: string) {
		selectedProton = value;
	}

	async function runTool(tool: string) {
		if (isLoading) return;
		if (!prefixPath) {
			notifications.error("Please select or create a prefix first.");
			return;
		}

		isLoading = true;
		runningToolName = tool;

		let cleanName = selectedProton;
		if (cleanName.startsWith("(Steam) ")) {
			cleanName = cleanName.substring(8);
		}
		try {
			await RunPrefixTool(prefixPath, tool, cleanName);
		} catch (err) {
			notifications.error(`Failed to run ${tool}: ${err}`);
		} finally {
			setTimeout(() => {
				isLoading = false;
				runningToolName = "";
			}, 500);
		}
	}

	$: currentPrefixName = prefixPath.startsWith(baseDir) ? prefixPath.replace(baseDir + "/", "") : prefixPath;
</script>

<div class="prefix-container">
	<h1 class="page-title">Prefix Manager</h1>
	<div class="main-layout">
		<div class="sidebar-section glass">
			<div class="section-header">
				<h2>Available Prefixes</h2>
			</div>
			<div class="prefix-list">
				{#each availablePrefixes as name}
					<button
						class="prefix-item"
						class:active={currentPrefixName === name}
						on:click={() => selectPrefix(name)}
					>
						<span class="folder-icon">üìÅ</span>
						<span class="name">{name}</span>
					</button>
				{/each}
				{#if availablePrefixes.length === 0}
					<div class="empty-state">No prefixes found in default directory.</div>
				{/if}
			</div>
			<div class="add-prefix-area">
				<input
					type="text"
					placeholder="New prefix..."
					bind:value={newPrefixName}
					class="input sm"
					on:keydown={(e) => e.key === "Enter" && handleCreatePrefix()}
				/>
				<button class="btn primary sm" on:click={handleCreatePrefix}>Create</button>
			</div>
		</div>
		<div class="content-section">
			<div class="config-card glass">
				<div class="form-group">
					<label for="prefixPath">Selected Prefix Path</label>
					<div class="input-group">
						<input id="prefixPath" type="text" class="input" bind:value={prefixPath} readonly />
						<button class="btn" on:click={handleBrowse}>Browse Other</button>
					</div>
				</div>
				<div class="form-group">
					<label for="protonRuntime">Runtime Environment (Proton)</label>
					<div id="protonRuntime">
						<Dropdown
							options={protonOptions}
							bind:value={selectedProton}
							onChange={handleProtonChange}
						/>
					</div>
				</div>
			</div>

			<div class="tools-grid">
				<ToolButton
					icon="‚öôÔ∏è"
					title="Winecfg"
					subtitle="Settings"
					loading={runningToolName === "winecfg"}
					onClick={() => runTool("winecfg")}
				/>
				<ToolButton
					icon="üìù"
					title="Regedit"
					subtitle="Registry"
					loading={runningToolName === "regedit"}
					onClick={() => runTool("regedit")}
				/>
				<ToolButton
					icon="üíª"
					title="CMD"
					subtitle="Terminal"
					loading={runningToolName === "cmd"}
					onClick={() => runTool("cmd")}
				/>
				<ToolButton
					icon="ü™Ñ"
					title="Winetricks"
					subtitle="Extras"
					loading={runningToolName === "winetricks"}
					onClick={() => runTool("winetricks")}
				/>
				<ToolButton
					icon="üìä"
					title="TaskMgr"
					subtitle="Processes"
					loading={runningToolName === "taskmgr"}
					onClick={() => runTool("taskmgr")}
				/>
				<ToolButton
					icon="üìÇ"
					title="Explorer"
					subtitle="Files"
					loading={runningToolName === "explorer"}
					onClick={() => runTool("explorer")}
				/>
			</div>

			<div class="config-card glass">
				<div class="section-header-row">
					<h3>Default Configuration</h3>
					<button class="btn primary sm" on:click={handleSaveConfig}>Save Defaults</button>
				</div>
				<div class="config-form-wrapper">
					<ConfigForm bind:options={prefixOptions} />
				</div>
			</div>
		</div>
	</div>
</div>

